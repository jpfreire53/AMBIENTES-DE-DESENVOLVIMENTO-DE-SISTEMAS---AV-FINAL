public class Soma {
    public int soma(int a, int b) {
        int resultado = a + b;
        System.out.println("A soma de " + a + " e " + b + " é: " + resultado);
        return resultado;
    }

    public int multiplica(int a, int b) {
        int resultado = a * b;
        System.out.println("A multiplicação de " + a + " e " + b + " é: " + resultado);
        return resultado;
    }
}
